SELECT 
    i.REST_ID, i.REST_NAME, i.FOOD_TYPE, i.FAVORITES, i.ADDRESS,  
    ROUND(AVG(r.REVIEW_SCORE), 2) as SCORE
FROM 
    REST_INFO i
JOIN 
    REST_REVIEW r ON r.REST_ID = i.REST_ID
WHERE
    i.ADDRESS LIKE '서울%'
GROUP BY
    i.REST_ID, i.REST_NAME, i.FOOD_TYPE, i.FAVORITES, i.ADDRESS
ORDER BY 
    SCORE DESC, i.FAVORITES DESC
;